    private void admitStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_admitStudentActionPerformed
        if(!RollNo.getText().isEmpty()&&students.getSelectedIndex()!=-1){
            UsersClass tempTeacher = null ;
            ArrayList<String> tempStudent = new ArrayList<>() ;
            timeTableClass tempTime = null ;
            for(UsersClass tempUser : User.getUsers().values()){
                if(tempUser.getJobStatus().equals("Student")){
                    if(tempUser.getName().equals(students.getSelectedItem())){
                        for(UsersClass TempUser : User.getUsers().values()){
                            if(TempUser.getJobStatus().equals("Teacher")){
                                for(timeTableClass tempTimeTable : ((TeacherClass)TempUser).getClasses().keySet()){
                                    if(previousSection.getSelectedItem().toString().equals(tempTimeTable.getSubject().getUserClass().getSection())&&semesterStudents.getSelectedItem().toString().equals(tempTimeTable.getSubject().getUserClass().getSemester())){
                                        ((StudentClass)tempUser).setRollNo(RollNo.getText());
                                        ((StudentClass)tempUser).addTimeTable(tempTimeTable);
                                        tempTeacher=TempUser;
                                        tempStudent.add(tempUser.getName());
                                        tempTime=tempTimeTable;
                                        break;
                                    }
                                }
                            }
                        }
                    }
                }
            }
            for(UsersClass tempUser : User.getUsers().values()){
                if(tempUser.equals(tempTeacher)){
                    ((TeacherClass)tempUser).setClasses(tempTime, tempStudent);
                }
            }
            section.setSelectedIndex(0);
            RollNo.setText("");
            students.removeItemAt(students.getSelectedIndex());
        }
        else
            JOptionPane.showMessageDialog(this, "Some fields are Empty");
    }//GEN-LAST:event_admitStudentActionPerformed

