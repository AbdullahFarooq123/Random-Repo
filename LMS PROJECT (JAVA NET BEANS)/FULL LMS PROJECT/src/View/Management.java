    private void generateTimeTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateTimeTableActionPerformed
        if(TeachersName2.getSelectedIndex()!=-1){
            if(!sections.getText().isBlank()&&!subjectName.getText().isBlank()&&!roomNumber.getText().isBlank()){
                for(UsersClass tempUser : User.getUsers().values()){
                    if(tempUser.getName().equals(TeachersName2.getSelectedItem().toString())){
                        ((TeacherClass)tempUser).setClasses(new timeTableClass(days.getSelectedItem().toString(),
                        hoursFrom.getSelectedItem()+":"+minutesFrom.getSelectedItem()+" "+dayFormatFrom.getSelectedItem()+
                        " TO "+ hoursTo.getSelectedItem()+":"+minutesTo.getSelectedItem()+" "+dayFormatTo.getSelectedItem(),
                        roomNumber.getText(),new SubjectClass(subjectName.getText(),new AttendanceClass(subjectName.getText())
                        ,new Lecture(semesters.getSelectedItem().toString(),sections.getText() ))), null);
                    }
                }
                String tempSection = sections.getText();
                boolean present = false ;
                for(int i = 0 ; i < section.getItemCount();i++){
                    if(section.getItemAt(i)==tempSection){
                        present = true ;
                    }
                }
                if(!present){
                    section.addItem(tempSection);
                    section.setSelectedIndex(0);
                    previousSection.addItem(tempSection);
                    previousSection.setSelectedIndex(0);
                }
                DefaultTableModel tableModel =(DefaultTableModel)timeTableTable.getModel();
                if(a==0)
                    tableModel.setValueAt("",days.getSelectedIndex(), ++a);
                String value = tableModel.getValueAt(days.getSelectedIndex(), 1).toString();
                tableModel.setValueAt(value.isEmpty()?setTimeTable():value + "\n\n" + setTimeTable(), days.getSelectedIndex(),1);       
                timeTableTable.setRowHeight(timeTableTable.getRowHeight()+50+(a+=10));
                sections.setText("");
                subjectName.setText("");
                roomNumber.setText("");
                TeachersName.setSelectedIndex(0);
            }
            else
                JOptionPane.showMessageDialog(this, "Some fields are empty!");
        }
        else
            JOptionPane.showMessageDialog(this, "No teachers!");
    }//GEN-LAST:event_generateTimeTableActionPerformed

