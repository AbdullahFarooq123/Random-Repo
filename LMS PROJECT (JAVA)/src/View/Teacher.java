    private void nextQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextQuestionActionPerformed
        if(!marksField.getText().isBlank()&&!marksField.getText().isBlank()){
        int noOfValues = Integer.parseInt((String)options.getSelectedItem());
        int marksOfQuestion=0;
        try{
            marksOfQuestion=Integer.parseInt(marksField.getText());
        }catch(Exception e){
            marksOfQuestion=1;
        }
        JRadioButton selectedButton=null;
        for(JRadioButton button : selectionOptions){
            if(button.isSelected()){
                selectedButton=button;
                break;
            }
        }
        if(selectedButton!=null){
        questionsArrayList.add(noOfQuestions,questionsType.getSelectedItem().toString().equals("MCQ's ")?new Question(questionTypeString, questions.getText(),marksOfQuestion, noOfValues,selectedButton ):new Question(questionTypeString, questions.getText(),marksOfQuestion) );
        questions.setText("");
        marksField.setText("");
        options.setSelectedIndex(0);
        questionsType.setSelectedIndex(0);
        String []options={"a)","b)","c)","d)"};
        for(int i = 0 ; i < noOfValues;i++){
            selectionOptions[i].setText(options[i]);
            selectionOptions[i].setSelected(false);
        }
        noOfQuestions++;
        selectionOptions[0].setVisible(true);
        selectionOptions[1].setVisible(false);
        selectionOptions[2].setVisible(false);
        selectionOptions[3].setVisible(false);
        }
        else 
            JOptionPane.showMessageDialog(this, "Please select the right option!");
        }
        else
            JOptionPane.showMessageDialog(this, "Please fill all the fields!");
    }//GEN-LAST:event_nextQuestionActionPerformed

