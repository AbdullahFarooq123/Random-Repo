    private void previousQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_previousQuestionActionPerformed
        if(questionsArrayList.size()!=0){
            --noOfQuestions;
            if(noOfQuestions>=0){
            Question question=questionsArrayList.get(noOfQuestions);
            questionsType.setSelectedItem(question.getQuestionType());
            questions.setText(question.getQuestion());
            marksField.setText(Integer.toString(question.getQuestionMarks()));
            options.setSelectedItem(question.getOptions());
            for(int i = 0 ; i < question.getOptions();i++){
                selectionOptions[i].setVisible(true);
                if(selectionOptions[i].getText().equals(question.getOption().getText()))
                    selectionOptions[i].setSelected(true);
            }
            }
            else noOfQuestions++;
        }
        System.out.println(paperLayerd.getBounds());
        paperLayerd.setBounds(0, 0, paperLayerd.getWidth(), paperLayerd.getHeight()+248);
    }//GEN-LAST:event_previousQuestionActionPerformed

