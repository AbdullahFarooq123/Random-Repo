    private void presentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_presentActionPerformed
        if(absent.isSelected())
            absent.setSelected(false);
        present.setSelected(true);
        User.markAttendance(User.getUsers().get(user).getName(),true);
        boolean contains = false;
        for(int i = 0 ; i < previousDates.getItemCount() ; i++ ){
            if(previousDates.getItemAt(i).toString().equals(getDateFormat(LocalDate.now()))){
                contains=true;
                break;
            }
        }
        if(!contains||previousDates.getItemCount()==0){
            previousDates.insertItemAt(getDateFormat(LocalDate.now()),0);
        }
        previousDates.setSelectedIndex(0);
    }//GEN-LAST:event_presentActionPerformed

