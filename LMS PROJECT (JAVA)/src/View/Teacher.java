    private void semester1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_semester1ActionPerformed
        DefaultTableModel tempModel = (DefaultTableModel)marksPanel.getModel();
        for(int i = 0 ; i < marksPanel.getRowCount();i++){
            tempModel.removeRow(i);
        }
        for(UsersClass tempUsers : User.getUsers().values()){
            if(tempUsers.getJobStatus().equals("Student")){
                for(timeTableClass tempTable : ((StudentClass)tempUsers).getStudentTimeTable()){
                    if((tempTable.getSubject().getSubjectName().equals(subject1.getSelectedItem().toString()))){
                        if(sections1.getItemCount()!=0)
                        if((tempTable.getSubject().getUserClass().getSection().equals(sections1.getSelectedItem().toString()))){
                            if(semester1.getItemCount()!=0)
                            if((tempTable.getSubject().getUserClass().getSemester().equals(semester1.getSelectedItem().toString()))){
                                
                                tempModel.addRow(new Object[]{
                                    tempUsers.getName(),
                                    ((StudentClass)tempUsers).getRollNo(),
                                    tempTable.getSubject().getMarks()                            
                                });
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_semester1ActionPerformed

    private void doneMarksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneMarksActionPerformed
        for(int i = 0 ; i < marksPanel.getRowCount();i++){
            for(UsersClass tempUsers : User.getUsers().values()){
                if(tempUsers.getJobStatus().equals("Student")){
                    if(tempUsers.getName().equals(marksPanel.getValueAt(i, 0).toString())){
                        for(timeTableClass tempTable : ((StudentClass)tempUsers).getStudentTimeTable()){
                            if(tempTable.getSubject().getSubjectName().equals(subject1.getSelectedItem().toString())){
                                if(tempTable.getSubject().getUserClass().getSection().equals(sections1.getSelectedItem().toString())){
                                    if(tempTable.getSubject().getUserClass().getSemester().equals(semester1.getSelectedItem())){
                                        tempTable.getSubject().setMarks(Double.parseDouble(marksPanel.getValueAt(i, 2).toString()));
                                        break;
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_doneMarksActionPerformed

