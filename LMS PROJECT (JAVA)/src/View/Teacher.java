    private void initializeDashBoard(){
        if(User.getAttendance().size()!=0){
            if(User.getAttendance().get(User.getUsers().get(user).getName())!=null){
                if(User.getAttendance().get(User.getUsers().get(user).getName()).containsKey(LocalDate.now())){
                    if(User.getAttendance().get(User.getUsers().get(user).getName()).get(LocalDate.now()))
                        present.setSelected(true);
                    else
                        absent.setSelected(true);
                }
                for(LocalDate date : User.getAttendance().get(User.getUsers().get(user).getName()).keySet())
                    previousDates.addItem(getDateFormat(date));
                
            }
        }
    }
    private String getDateFormat(LocalDate date){
        DateTimeFormatter dateFormat = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        return dateFormat.format(date);
    }
    private void intitialize(){
        UsersClass tempUser = User.getUser(user);
        HashSet<String>tempSections=new HashSet<>();
        HashSet<String>tempSemester=new HashSet<>();
        HashSet<String>tempSubject=new HashSet<>();
        for(timeTableClass tempClass : ((TeacherClass)tempUser).getClasses().keySet()){
            tempSubject.add(tempClass.getSubject().getSubjectName());
            tempSections.add(tempClass.getSubject().getUserClass().getSection());
            tempSemester.add(tempClass.getSubject().getUserClass().getSemester());
        }
        for(String tempValue : tempSubject)
            subject1.addItem(tempValue);
        for(String tempValue : tempSections)
            sections1.addItem(tempValue);
        for(String tempValue : tempSemester)
            semester1.addItem(tempValue);
        if(subject1.getItemCount()!=0)
            subject1.setSelectedIndex(0);
        if(sections1.getItemCount()!=0)
            sections1.setSelectedIndex(0);
        if(semester1.getItemCount()!=0)
            semester1.setSelectedIndex(0);
    }
    
